class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int n=arr.size();
        sort(arr.begin(), arr.end());
        int ans=-1;
        while(n>1){
            if(arr[n-1]!=arr[n-2]){
                ans=arr[n-2];
                break;
            }
            n--;
        }
        return ans;
    }
};

